export function onUpgradePurchased(upgradeId: string): string {
  const direct = LINES[upgradeId];
  if (direct) return direct;
  const u = UPGRADES.find((x) => x.id === upgradeId);
  if (u) return ZONE_FALLBACK[u.zone] ?? "Отличная покупка!";
  return "Отличная покупка!";
}
